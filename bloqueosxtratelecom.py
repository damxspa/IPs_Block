ARCHIVO_SALIDA = "bloqueosip.txt"
bloqueadas = 0
MAX_IPS_POR_RANGO = 256  # Lmite de IPs a analizar por rango CIDR
MAX_TRABAJADORES = 50  # Nmero mximo de hilos para anlisis paralelo
TIMEOUT = 3  # Timeout en segundos para conexin al puerto

def generar_ips_desde_cidr(cidr):
    """Genera una lista de IPs a partir de un rango CIDR, con lmite."""
    try:
        red = ipaddress.ip_network(cidr, strict=False)
        total_ips = red.num_addresses
        if total_ips > MAX_IPS_POR_RANGO:
            print(f" Rango {cidr} tiene {total_ips} IPs, limitando a {MAX_IPS_POR_RANGO}.")
        return [str(ip) for ip in list(red)[:MAX_IPS_POR_RANGO]]
    except ValueError as e:
        print(f"Error en el rango CIDR {cidr}: {e}")
        return []

def analizar_ip(ip):
    """Analiza si el puerto 80 de la IP est bloqueado."""
    global bloqueadas
    try:
        # Crear un socket TCP
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(TIMEOUT)
        # Intentar conectar al puerto 80
        result = sock.connect_ex((ip, 80))
        sock.close()
        # Si result != 0, la conexin fall (puerto bloqueado o no disponible)
        if result != 0:
            timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
            linea = f"{timestamp} {ip}\n"
            with open(ARCHIVO_SALIDA, "a") as f:
                f.write(linea)
            print(f" PUERTO 80 BLOQUEADO: {ip}")
            return 1
    except Exception as e:
        # Cualquier excepcin (timeout, error de conexin, etc.) se considera puerto bloqueado
        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        linea = f"{timestamp} {ip}\n"
        with open(ARCHIVO_SALIDA, "a") as f:
            f.write(linea)
        print(f" PUERTO 80 BLOQUEADO: {ip} (Error: {str(e)})")
        return 1
    return 0

def analizar_ips_paralelo(ips):
    """Analiza una lista de IPs en paralelo usando ThreadPoolExecutor."""
    global bloqueadas
    with ThreadPoolExecutor(max_workers=MAX_TRABAJADORES) as executor:
        futuros = [executor.submit(analizar_ip, ip) for ip in ips]
        for futuro in as_completed(futuros):
            bloqueadas += futuro.result()

if __name__ == "__main__":
    print(" Analizando IPs prioritarias y rangos CIDR para puerto 80...\n")

    # Analizar las IPs prioritarias en paralelo
    print(" Escaneando IPs prioritarias...")
    analizar_ips_paralelo(IPS_PRIORITARIAS)

    # Generar y analizar IPs desde los rangos CIDR
    print("\n Escaneando rangos CIDR...")
    for cidr in RANGOS_CIDR:
        print(f"Procesando rango {cidr}...")
        ips_en_rango = generar_ips_desde_cidr(cidr)
        if ips_en_rango:
            analizar_ips_paralelo(ips_en_rango)

    resumen = f"\n Escaneo completado. Total de IPs con puerto 80 bloqueado: {bloqueadas}\n"
    print(resumen)

    with open(ARCHIVO_SALIDA, "a") as f:
        f.write(resumen)
